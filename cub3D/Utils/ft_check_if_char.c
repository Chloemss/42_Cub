#include "../cub3D.h"

bool	ft_check_if_char(char *line)
{
	int	i;

	i = 0;
	while (line[i] && line)
	{
		if (line[i] > 32 && line[i] != 127)
			return (1);
		i++;
	}
	return (0);
}
